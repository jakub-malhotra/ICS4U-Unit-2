public class Airplane {
    /*
    * The speed of the plane
    */
    private int speed;

    /**
     * Gets the speed of the airplane.
     * 
     * @return the speed of the airplane
     */
    public int getSpeed() {
        return speed;
    }

    /**
     * Sets the speed of the airplane.
     * 
     * @param speed the speed to set
     */
    public void setSpeed(int speed) {
        this.speed = speed;
    }
}

